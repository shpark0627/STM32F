void HAL_UART_RxCplitCallback(UART_HandleTypeDef* huart){
	if(huart->Instance == USART2){
		if (pc_data == '\t' || pc_data == '0x09'){
			id_mode = 1;
			pc_index = 0;

			char msg[] = "\r\nInput ID: \r\n";
			HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 100);
		}
		else if(pc_data == '\r' || pc_data == '\n'){
			if(pc_index > 0){
				pc_buffer[pc_index] = '\0';


				if(id_mode == 1){
					strcpy(id_buffer, pc_buffer);
					id_mode = 0;

					char log_msg[];
					sprintf(log_msg, "\r\n[system] ID - > %s", id_buffer);
					HAL_UART_Transmit(&hurat2, (uint8_t*)log_msg, strlen(log_msg), 100);

				}
				pc_index = 0;
			}


		}

		else{
			pc_buffer[pc_index] = pc_data;
			pc_index++;
			HAL_UART_Transmit(&huart2, &pc_buffer, pc_index, 100);
		}
		HAL_UART_Transmit(&huart2, &pc_buffer, pc_index, 100);
		HAL_UART_Transmit(&huart1, &pc_buffer, pc_index, 100);
		HAL_UART_Receive_IT(&huart2, &pc_buffer, 1);
	}

	if(huart->Instance == USART1){
		HAL_UART_Transmit(&huart2, &board_buffer, board_index, 100);
		HAL_UART_Receive_IT(&huart1, &board_buffer, 1);

	}
}
